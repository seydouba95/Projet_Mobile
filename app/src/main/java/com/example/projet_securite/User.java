package com.example.projet_securite;

public class User {


        public String email;
        public String nom;
        public String addresse;
        public String telephone;


        public User() {
        }

    public User(String email, String nom, String addresse, String telephone) {
        this.email = email;
        this.nom = nom;
        this.addresse = addresse;
        this.telephone = telephone;
    }
}


